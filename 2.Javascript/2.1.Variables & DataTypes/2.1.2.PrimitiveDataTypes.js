
// JavaScript Variables string, number, bigint, boolean, undefined, null, symbol
// JavaScript variables are containers for storing data values.
// In this example, we are creating variables of different data types.
// We are using the let keyword to declare variables.

// JavaScript Variables string
let greeting = "Hello, world!";
console.log(greeting); // Output: Hello, world!

// JavaScript Variables number
let age = 25;
console.log(age); // Output: 25

// JavaScript Variables bigint
let bigNumber = 123456789012345678901234567890n;
console.log(bigNumber); // Output: 123456789012345678901234567890n

// JavaScript Variables boolean
let isTrue = true;
let isFalse = false;
console.log(isTrue); // Output: true
console.log(isFalse); // Output: false

// JavaScript Variables undefined
let name;
console.log(name); // Output: undefined

const id = Symbol('uniqueId');
console.log(id); // Output: Symbol(uniqueId)

// JavaScript Variables null
let data = null;
console.log(data); // Output: null

